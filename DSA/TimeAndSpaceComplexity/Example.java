package DSA.TimeAndSpaceComplexity;

public class Example {
    void printNumbers(int[] array) {
        for (int num : array) {
            System.out.println(num);
        }
    }
}

/*
 * In Java, you can analyze time complexity by considering the number of basic
 * operations (such as comparisons, assignments, arithmetic operations) executed
 * by an algorithm as a function of the input size. It's important to understand
 * the time complexity of an algorithm to choose the most efficient one for
 * aparticular problem In this example, the time complexity of the printNumbers
 * method is O(n), where 'n' is the size of the input array.
 */

