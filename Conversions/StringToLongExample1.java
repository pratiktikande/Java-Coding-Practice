package Conversions;

//Method 2: Using Long.valueOf()

public class StringToLongExample1 {
    public static void main(String[] args) {
        String numericString = "987654321";
        long convertedLong = Long.valueOf(numericString);
        System.out.println("Converted long value: " + convertedLong);
    }
}


