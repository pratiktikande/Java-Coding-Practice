package New_folder;

public class HalfPyramid {
    public static void main(String[] args) {
        //OuterLoop 
        for(int i=1; i<=4; i++){
            //inner Loop
            for(int j=1; j<=i; j++){
                //use J as counter to print
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
